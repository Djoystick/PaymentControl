type LocalizationContextValue = {
  language: UiLanguage;
  setLanguage: (next: UiLanguage) => void;
  tr: (text: string, params?: Record<string, string | number>) => string;
};

const LocalizationContext = createContext<LocalizationContextValue | null>(null);

const isUiLanguage = (value: string | null): value is UiLanguage => {
  return value === "en" || value === "ru";
};

const resolveClientLanguage = (): UiLanguage => {
  try {
    const stored = window.localStorage.getItem(STORAGE_KEY);
    if (isUiLanguage(stored)) {
      return stored;
    }
  } catch {
    // Ignore storage read errors.
  }

  const browserLanguage = window.navigator.language?.toLowerCase() ?? "";
  if (browserLanguage.startsWith("ru")) {
    return "ru";
  }

  return "en";
};

const interpolate = (
  text: string,
  params?: Record<string, string | number>,
): string => {
  if (!params) {
    return text;
  }

  return text.replace(/\{(\w+)\}/g, (_, key: string) => {
    const value = params[key];
    return value === undefined ? `{${key}}` : String(value);
  });
};

export function LocalizationProvider({ children }: { children: ReactNode }) {
  const [language, setLanguageState] = useState<UiLanguage>("en");
  const [isLanguageHydrated, setIsLanguageHydrated] = useState(false);

  useEffect(() => {
    const frame = window.requestAnimationFrame(() => {
      const resolvedLanguage = resolveClientLanguage();
      setLanguageState(resolvedLanguage);
      setIsLanguageHydrated(true);
    });

    return () => {
      window.cancelAnimationFrame(frame);
    };
  }, []);

  useEffect(() => {
    if (!isLanguageHydrated) {
      return;
    }

    try {
      window.localStorage.setItem(STORAGE_KEY, language);
    } catch {
      // Ignore storage write errors.
    }
  }, [isLanguageHydrated, language]);

  const setLanguage = useCallback((next: UiLanguage) => {
    setLanguageState(next);
  }, []);

  const tr = useCallback(
    (text: string, params?: Record<string, string | number>) => {
      if (language === "ru") {
        return interpolate(ruTranslations[text] ?? text, params);
      }

      return interpolate(text, params);
    },
    [language],
  );

  const value = useMemo(
    () => ({
      language,
      setLanguage,
      tr,
    }),
    [language, setLanguage, tr],
  );

  return (
    <LocalizationContext.Provider value={value}>
      {children}
    </LocalizationContext.Provider>
  );
}

export const useLocalization = (): LocalizationContextValue => {
  const context = useContext(LocalizationContext);
  if (!context) {
    throw new Error("useLocalization must be used inside LocalizationProvider");
  }

  return context;
};
